public class Multiplication {
    private Integer a, b;

    public Multiplication() {
    }

    public Integer Multiplication() {
        return a*b;
    }

    public Integer getA() {
        return a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Integer getB() {
        return b;
    }

    public void setB(Integer b) {
        this.b = b;
    }
}

